package com.faction.elements;


/**
 * Campaign Class used to get the assessment campaign
 */
public class Campaign{
	

	private Long id;
	private String name;
	public Long getId() {
		return id;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
